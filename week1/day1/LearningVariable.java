package jul22.week1.day1;

public class LearningVariable {

	
	public static void main(String[] args) {
		//dataType dataTypeName (or) Variable name = value;
		int a = 10;
		int b = 20;
		int sum = a+b;
		System.out.println(sum);
		
		byte c = 10;
		short d = 20;
		long l = 20L;
		boolean e = true;
		float f = 10f;
		double s = 10.55;
		
		System.out.println("byte value: "+c);
		System.out.println("short value: " + d);
		System.out.println("long value: " + l);
		System.out.println("Boolean value:" + e);
		System.out.println("float value: "+ f);
		System.out.println("double value: " + s);
		
		String name ="Globale ERP";
		System.out.println(name);
	}

}
